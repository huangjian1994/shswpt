package com.htcf.entity;

public class FirstUrlObject {
	
	private String code;
	private String firsturlname;
	private String firsturl;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getFirsturl() {
		return firsturl;
	}

	public void setFirsturl(String firsturl) {
		this.firsturl = firsturl;
	}

	public String getFirsturlname() {
		return firsturlname;
	}

	public void setFirsturlname(String firsturlname) {
		this.firsturlname = firsturlname;
	}

	
	
}
